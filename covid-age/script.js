function refreshDisplay() {
  display.innerText = `${age} + ${modifier} = ${Number(age) + modifier}`;
}

function updateAge() {
  let others = othMod.reduce((a, b) => a + b, 0);
  modifier = sexMod + ethMod + bmiMod + athMod + diaMod + kidMod + nbcMod + bldMod + hrtMod + others;
  covidAge = Number(age) + modifier;
  if (covidAge >= 85) {
    covidAge = "85+";
  }
  if (modifier >= 0) {
    display.innerText = `${age} + ${modifier} = ${covidAge}`;
  } else {
    display.innerText = `${age} - ${Math.abs(modifier)} = ${covidAge}`;
  }
  document.querySelector("#tbl_total").innerText = modifier;
  document.querySelector("#below_20").classList.add("d-none");
  if (covidAge < 20) {
    document.querySelector("#below_20").classList.remove("d-none");
  }
  updateIFR();
}

function calcImpBMI() {
  let ftVal = document.querySelector("#imp_feet").value * 12;
  let inVal = document.querySelector("#imp_inch").value;
  let stVal = document.querySelector("#imp_stn").value * 14;
  let lbVal = document.querySelector("#imp_lbs").value;
  let totalIn = Number(inVal) + Number(ftVal);
  let totalLb = Number(stVal) + Number(lbVal);
  let rtnBMI = (totalLb * 703) / totalIn ** 2;
  return Math.round(rtnBMI * 10) / 10;
}

function calcMetBMI() {
  let kgVal = document.querySelector("#met_kg").value;
  let mVal = document.querySelector("#met_m").value;
  let rtnBMI = Number(kgVal) / Number(mVal) ** 2;
  return Math.round(rtnBMI * 10) / 10;
}

function calcBMIcat(bmi) {
  if (bmi < 30) {
    return "none";
  } else if (bmi < 35) {
    return "bmi1";
  } else if (bmi < 40) {
    return "bmi2";
  } else if (bmi > 40) {
    return "bmi3";
  } else {
    return "none";
  }
}

